import datetime as dt


class Record:
    # [1]: Вместо date='' используй date=None
    # Это хорошая привычка. Использование пустых коллекций однажды может привести к неожиданным багам.
    # [2]: Аннотация типов
    # Это тоже из раздела хороших привычек. Указание аннотаций типов не убережёт тебя от того, что ты передашь вместо str, например, int.
    # Но это уже часть документирования кода, и будет уже некоторой подсказкой при вызове метода, что необходжимо будет передать.
    # Учитывая [1] и [2], рекомендую объявить так:
    # -----------------------------------------------------------------------
    # def __init__(self, amount: int, comment: str, date: str | None = None):
    # -----------------------------------------------------------------------
    # Пункт с аннотацией типов не только здесь, рекомендую применять во всём коде
    def __init__(self, amount, comment, date=''):
        self.amount = amount
        # [3]: Тернарный оператор
        # Круто, что ты его знаешь! Он красив, но данном случае я бы рекомендовал вынести его во внешний if, то есть:
        # -----------------------------------------------------------------------
        # if not date:
        #     date = dt.datetime.now().date()
        # else:
        #     date = dt.datetime.strptime(date, '%d.%m.%Y').date()
        # self.date = date
        # -----------------------------------------------------------------------
        # Философия Python гласит, что явное лучше неявного, и не нужно усложнять
        self.date = (
            dt.datetime.now().date() if
            not
            date else dt.datetime.strptime(date, '%d.%m.%Y').date())
        self.comment = comment


class Calculator:
    def __init__(self, limit):
        self.limit = limit
        self.records = []

    def add_record(self, record):
        self.records.append(record)

    def get_today_stats(self):
        # [4] Задачка со звёздочкой
        # Можно решить задачу суммирования без использования переменной счётчика.
        # Дам подсказку - sum() и генераторное выражение.
        # Так, как реализовано сейчас - супер, это работает, но можно сделать прям на уровне "вау".
        # Аналогично, можно реализовать для следующего метода - get_week_stats()
        today_stats = 0
        # [5] for Record - некорректное наименование.
        # Во-первых, согласно PEP8, переменные именуются с маленьких букв, классы с заглавных.
        # Во-вторых, у тебя уже создан класс Record. Если бы потребовалось внутри данного метода создавать объект этого класса,
        # это привело бы к ошибке.
        for Record in self.records:
            # [6] Смотри, в этом месте ты уже проходишь по списку.
            # В следующем условии ты сравниваешь дату с текущей, для поиска записей сегодняшнего дня.
            # И получается, что каждую итерацию прохождения по списку метод dt.datetime.now().date() вызывается снова и снова.
            # Стоит создать отдельную переменную, в которую можно положить значение (там же, где ты объявляешь счётчик today_stats),
            # и далее каждую итерацию прохода по списку работать с уже расчитанным значением
            if Record.date == dt.datetime.now().date():
                today_stats = today_stats + Record.amount
        return today_stats

    def get_week_stats(self):
        week_stats = 0
        today = dt.datetime.now().date()
        for record in self.records:
            # [7] С точки зрения логики всё верно
            # Смотри, как можно видоизменить это условие:
            # -----------------------------------------------------------------------
            # if 0 <= (today - record.date).days < 7:
            # -----------------------------------------------------------------------
            # Условие точно такое же, при этом оно уже читается в одну строку
            # При этом, даже в такой записи при вычислении условия (today - record.date).days будет выполняться дважды.
            # Вынеси (today - record.date).days в отдельную переменную внутри цикла перед if'ом, и попробуй написать в виде цепочки условий.
            if (
                (today - record.date).days < 7 and
                (today - record.date).days >= 0
            ):
                week_stats += record.amount
        return week_stats


