class CashCalculator(Calculator):
    # [13] Наименование констант само за себя говорящее, комментарии к курсам валют избыточны
    # [14] float(60) - да, это работает, но можно просто написать USD_RATE = 60.0
    USD_RATE = float(60)  # Курс доллар США.
    EURO_RATE = float(70)  # Курс Евро.

    # [15] А вот тут очень критичный момент!
    # Необходимо определиться с курсом валют.
    # Тут или используешь константы и не передаёшь их при инициализации объекта класса,
    # или это не константы, тогда они должны быть наименованы в соответствии с PEP8.
    # Сейчас получается, что это вроде бы и константы, но ты можешь их переопределить при
    # инициализации объекта класса.
    # [16] Параметры метода.
    # Или пиши их в одну строку, или если выходишь за пределы количества символов в одной строке,
    # пиши каждый параметр на своей строке.
    def get_today_cash_remained(self, currency,
                                USD_RATE=USD_RATE, EURO_RATE=EURO_RATE):
        # [17] Следующее присвоение лишнее
        # У тебя дальше в условиях в переменную currency_type присваивается значение,
        # причём ни в одном из вариантов оно не равно значению currency.
        currency_type = currency
        cash_remained = self.limit - self.get_today_stats()
        if currency == 'usd':
            cash_remained /= USD_RATE
            currency_type = 'USD'
        elif currency_type == 'eur':
            cash_remained /= EURO_RATE
            currency_type = 'Euro'
        elif currency_type == 'rub':
            # [18] Операторы '=' и '==' разные!
            # '==' - сравнение
            # '='  - присвоение
            # В следующей строке ты просто сравниваешь cash_remained и 1.0,
            # значение не будет записано в cash_remained.
            cash_remained == 1.00
            currency_type = 'руб'
        # [19] А если на вход будет передано curency='abc'?
        # Стоит обработать этот случай.
        # Достаточно написать else, который поднимет исключение.
        # Это закроет все остальные случаи.
        if cash_remained > 0:
            return (
                f'На сегодня осталось {round(cash_remained, 2)} '
                f'{currency_type}'
            )
        elif cash_remained == 0:
            return 'Денег нет, держись'
        # [20] Выше ты проверил cash_remained на "больше 0", затем на "равно 0".
        # Последний вариант "меньше 0" единственный, в этом месте elif или else не нужны,
        # оставь просто return
        elif cash_remained < 0:
            # Смотри [10]. Здесь аналогично, в требованиях указано про бэкслеши в переносах.
            # Дополнительно, до этого ты применяешь f-строки.
            # Не нарушай консистентность, об этом сказано в требованиях к коду.
            return 'Денег нет, держись:' \
                   ' твой долг - {0:.2f} {1}'.format(-cash_remained,
                                                     currency_type)

    # [21]
    # В этом методе ты обращаешься к методу, определенному в родительском классе, и ты правильно к нему обратился.
    # При этом, ты переопределил родительский метод, и теперь он ничего не возвращает.
    # Здесь нет return, этот метод всегда будет возвращать None.
    # -----------------------------------------------------------------------
    # def get_week_stats(self):
    #     return super().get_week_stats()
    # -----------------------------------------------------------------------
    # Такая реализация будет правильной, но тут тоже есть важный момент: сам метод никак не изменился,
    # и по сути это бессмысленные строки.
    # Метод get_week_stats просто наследуется от класса Calculator, и если ты не меняешь его поведение в этом
    # классе-потомке, то не нужно его переопределять.
    def get_week_stats(self):
        super().get_week_stats()
